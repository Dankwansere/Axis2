export class Utility {

    /**
     * Compares two strings
     * @param str1 string 1 to compare
     * @param str2 string 2 to compare
     */
    static compareTwoStrings(str1: string, str2: string): boolean {

        if (str1 === str2) {
            return true;
        } else {
            return false;
        }

    }
}
